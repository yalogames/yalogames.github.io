function showGame(gList) {
    const divContainer = document.createElement('div');
    divContainer.className = "row";
    gList.forEach(element => {
        
        const div = document.createElement('div');
        div.classList.add('col-md-2', 'col-sm-3', 'col-4');
        div.innerHTML = `<a class="gc2" href="${element.pageUrl}">
                <img
                    alt="YaloGames | Unblocked Games Premium"
                    src="${element.imageUrl}"/>
                <h6>${element.name}</h6>
                </a>`;
        divContainer.appendChild(div);
    });
    return divContainer;

}

// Function to toggle the search input bar
function toggleSearch() {
  try {
    var container = document.getElementById('search-container');
    if (!container) return;

    var existing = document.getElementById('site-search-wrapper');
    if (existing) {
      existing.parentNode.removeChild(existing);
      return;
    }

    var wrapper = document.createElement('div');
    wrapper.id = 'site-search-wrapper';
    wrapper.style.padding = '10px';
    wrapper.style.width = '100%';
    wrapper.style.display = 'flex';
    wrapper.style.justifyContent = 'center';

    var input = document.createElement('input');
    input.id = 'site-search-input';
    input.type = 'search';
    input.placeholder = 'Search games...';
    input.style.padding = '8px';
    input.style.width = '60%';
    input.style.maxWidth = '400px';
    input.style.marginRight = '8px';
    input.style.border = '1px solid #ccc';
    input.style.borderRadius = '4px';

    var btn = document.createElement('button');
    btn.textContent = 'Search';
    btn.style.padding = '8px 12px';
    btn.style.border = 'none';
    btn.style.background = '#ffbd59';
    btn.style.color = '#000';
    btn.style.borderRadius = '4px';
    btn.style.cursor = 'pointer';

    btn.addEventListener('click', function () {
      var q = input.value.trim();
      //if (!q) return;
      // Go to a search page if you have one, otherwise do a site search on Google
      // Change the URL below to your internal search handler if available
      const query = input.value.trim();
        if (query) {
            window.location.href = `/?s=${encodeURIComponent(query)}`;
        } else {
            alert("Please enter a game name to search.");
        }
      //window.location.href = '/search.html?q=' + encodeURIComponent(q);
    });

    input.addEventListener('keydown', function (e) {
      if (e.key === 'Enter') btn.click();
      if (e.key === 'Escape') {
        if (wrapper && wrapper.parentNode) wrapper.parentNode.removeChild(wrapper);
      }
    });

    wrapper.appendChild(input);
    wrapper.appendChild(btn);

    // place wrapper at the start of container
    container.insertBefore(wrapper, container.firstChild);
    input.focus();
  } catch (err) {
    console.error('toggleSearch error:', err);
  }
}
